
const {ccclass, property} = cc._decorator;

/**
 * 每日任务配置
 */
@ccclass
export default class DailyTaskConfig {

    //每日分配最多多少任务
    static MAX_COUNT:number = 3;


    //每日是否重置。<br/>
    //如果设置重置，新的一天会把前一天的任务清除；<br/>
    //如果不重置，新的一天会把前一天已完成的任务清除。
    static RESET_DAILY:boolean = false;


    //每日任务中是否容许出现相同的任务
    static ALLOW_SAME_TASK:boolean = false;
}
